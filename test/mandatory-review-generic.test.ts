    "file --uncompress archive.gz",
    "file --no-sandbox README.md",
    "file -f local-list",
    "sort --files0-from=local-list",
    "wc --files0-from=local-list",
    "shasum -c checksums.txt",
    "cksum --check checksums.txt",
    "sort -T. input",
    "sort --temporary-directory=. input",
    "ffprobe -o report.json input.mov",
    "ffprobe -report input.mov",
    "date 01010000",
    "date -s tomorrow",
    "date --set=tomorrow",
    "git whatchanged --output=/tmp/out -1",
    "git whatchanged --ext-diff -1",
